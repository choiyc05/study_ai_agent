# 구분(승차, 하차) 별 이용객 합계 구하기
USE edu;

SELECT `날짜`, `구분`, (`05~06` + `06~07` + `07~08` + `08~09` + `09~10` + `10~11` + `11~12` + `12~13` + `13~14` + `14~15` + `15~16` + `16~17` + `17~18` + `18~19` + `19~20` + `20~21` + `21~22` + `22~23` + `23~24` + `24~`) AS sum
	FROM edu.seoul_metro_08_16
	WHERE `날짜` = '2008-01-01'
		AND `역번호` = 150
; 

USE edu;

SELECT `날짜`, `역번호`, `역명`, SUM(col1) AS 승차, SUM(col2) AS 하차 
	FROM (

		SELECT `날짜`, `역번호`, `역명`, 
			`05~06` + `06~07` +  `07~08` + `08~09` + `09~10` +  `10~11` +  `11~12` +  `12~13` + 
			`13~14` +  `14~15` + `15~16` +  `16~17` +  `17~18` +  `18~19` +  `19~20` +  `20~21` + 
			`21~22` +  `22~23` +  `23~24` +  `24~` AS col1, 
			0 AS col2
			FROM edu.seoul_metro_08_16
			WHERE `구분` = '승차'
		UNION 
		SELECT `날짜`, `역번호`, `역명`, 0, 
			`05~06` + `06~07` +  `07~08` + `08~09` + `09~10` +  `10~11` +  `11~12` +  `12~13` + 
			`13~14` +  `14~15` + `15~16` +  `16~17` +  `17~18` +  `18~19` +  `19~20` +  `20~21` + 
			`21~22` +  `22~23` +  `23~24` +  `24~`
			FROM edu.seoul_metro_08_16
			WHERE `구분` = '하차'
		
	) AS t
		
	WHERE `날짜` = '2008-01-01'	
	GROUP BY `날짜`, `역번호`, `역명`
;

CREATE VIEW sm_승차 AS
	SELECT
		`날짜`, `역번호`, `역명`, 
		`05~06` + `06~07` +  `07~08` + `08~09` + `09~10` +  `10~11` +  `11~12` +  `12~13` + 
		`13~14` +  `14~15` + `15~16` +  `16~17` +  `17~18` +  `18~19` +  `19~20` +  `20~21` + 
		`21~22` +  `22~23` +  `23~24` +  `24~` AS col1, 
		0 AS col2
		FROM edu.seoul_metro_08_16
		WHERE `구분` = '승차'
;

CREATE VIEW sm_하차 AS
	SELECT
		`날짜`, `역번호`, `역명`, 
		0 AS col1,
		`05~06` + `06~07` +  `07~08` + `08~09` + `09~10` +  `10~11` +  `11~12` +  `12~13` + 
		`13~14` +  `14~15` + `15~16` +  `16~17` +  `17~18` +  `18~19` +  `19~20` +  `20~21` + 
		`21~22` +  `22~23` +  `23~24` +  `24~` AS col2 
		FROM edu.seoul_metro_08_16
		WHERE `구분` = '하차'
;

SELECT *
	FROM `sm_승차`
	WHERE `날짜` = '2008-01-01';

CREATE VIEW `sm_total` AS
	SELECT `날짜`,
			`역번호`,
			`역명`,
			`구분`,
	(`05~06` + `06~07` + `07~08` + `08~09` + `09~10` + `10~11` + `11~12` + `12~13` + `13~14` + `14~15` + `15~16` + `16~17` + `17~18` + `18~19` + `19~20` + `20~21` + `21~22` + `22~23` + `23~24` + `24~` )
	AS `total` 
	FROM `seoul_metro_08_16`
;

SELECT `날짜`, `역번호`, `역명`, 
	SUM(CASE WHEN `구분` = '승차' THEN total ELSE 0 END) AS 승차,
	SUM(CASE WHEN `구분` = '하차' THEN total ELSE 0 END) AS 하차
	FROM edu.`sm_total`
	WHERE `날짜` = '2008-01-01'
	GROUP BY `날짜`, `역번호`, `역명`
;

SELECT `날짜`,
	`역번호`,
	`역명`,
	`구분`,
	(`05~06` + `06~07` + `07~08` + `08~09` + `09~10` + `10~11` + `11~12` + 
		`12~13` + `13~14` + `14~15` + `15~16` + `16~17` + `17~18` + `18~19` + 
		`19~20` + `20~21` + `21~22` + `22~23` + `23~24` + `24~` ) AS `total` 
	FROM `seoul_metro_08_16`
	WHERE `구분` = '승차'
;	

